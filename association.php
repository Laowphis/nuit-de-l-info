	<h2>Médecins Sans Frontières</h2>
	<p>Médecins Sans Frontières a été créée le 20 décembre 1971 par des médecins français qui s'étaient rendus au Biafra avec la Croix-Rouge pour tenter 
		d'y aider la population lors de la guerre qui avait opposé cette région indépendantiste au gouvernement central nigerian entre 1967 et 1970. 
		Ils voulurent fonder une association qui allierait aide humanitaire et actions de sensibilisation auprès des 
		médias et des institutions politiques. Médecins Sans Frontières interviennent lors de :
		<br>
		<ul>
			<li>Populations victimes de conflits</li>
			<li>Epidémies, endémies, pandémies</li>
			<li>Catastrophes naturelles</li>
			<li>Exclusion des soins</li>
		</ul>
	</p>
	<img src="./img/MSF.jpg" alt="Médecins Sans Frontières"><br><br>
	Site web : <a href="http://www.msf.fr/">http://www.msf.fr/</a>
