	<h2>De l'Eau Pour L'Afrique</h2>
	<p>L’association De l’eau pour l’Afrique est une association qui favorise l’accès à l’eau potable dans les pays en voie de développement.
		Malgré son jeune âge et le peu de budget dont elle dispose, l’association gravit peu à peu les marches et commence à grandir dans le monde de l’Humanitaire.
		Elle s'occupe essentiellement du traitement de l'eau et effectue leur mission sur les pays touchés par les problèmes d'approvisionnement et de distribution 
		et d'accès d'eau potable. Les aides que l'association propose :
		<br>
		<ul>
			<li>Accès à l'eau potable</li>
			<li>Creuser des puits</li>
			<li>Accès à l'eau pour l'agriculture</li>
			<li>Information face aux dangers du réassèchements du sol</li>
		</ul>
	</p>
