	<h2>Enfants Du Monde</h2>
	<p>Un développement sain pour un enfant requiert bien sûr plusieurs domaines d’intervention complémentaires et indissociables.
		Depuis plusieurs années, Enfants Du Monde avons mis l’accent sur la lutte contre la malnutrition, difficile pour un enfant de grandir, 
		guérir et apprendre sans manger à sa faim. Enfants du Monde s’est donc spécialisée dans la distribution de compléments alimentaires, 
		tout en portant en parallèle une attention particulière à des projets dans des domaines connexes tels :
		<br>
		<ul>
			<li>Constructions de cantines</li>
			<li>Développement des écoles</li>
			<li>Soutien aux enfants sourds</li>
			<li>Accès à l'eau potable</li>
		</ul>
	</p>
